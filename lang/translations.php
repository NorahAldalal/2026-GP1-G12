function getLang(): string {
    if (session_status() === PHP_SESSION_NONE) session_start();
    return $_SESSION['lang'] ?? 'en';
}

function t(string $key): string {
    global $__translations;
    $lang = getLang();
    return $__translations[$lang][$key] ?? $__translations['en'][$key] ?? $key;
}

function isRTL(): bool { return getLang() === 'ar'; }
function dir(): string { return isRTL() ? 'rtl' : 'ltr'; }
function langClass(): string { return isRTL() ? 'rtl' : ''; }

